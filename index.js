// requireで必要なクラスを用意
const Constants = require('./config');
const AIData = require('./ai_data');
const Player = require('./Player');
const Game = require('./GameMaster');
// クラスをオブジェクトとして生成
let game = new Game();
let player1 = new Player();
let player2 = new Player();

// units:unitの種類　[キング,クイーン,ナイト,ビショップ]など. ai:AI構築のデータ. 
player1.SetData(AIData.units0, AIData.ai0);
player2.SetData(AIData.units1, AIData.ai1);

// 対戦と結果
let winner = game.Play(player1, player2);
console.log("TIME:"+game.times);
console.log("勝者:"+winner);
console.log("RED LEFT:"+game.My_Left_Count);
console.log("BLUE LEFT:"+game.Enemy_Left_Count);
console.log("RED HP:"+game.My_KING_HP);
console.log("BLUE HP:"+game.Enemy_KING_HP);




